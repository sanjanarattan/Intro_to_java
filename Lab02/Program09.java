class Program09 {
	public static void main(String[] args){
		// char data type
		char a = (char) '\u0061';
		System.out.print(a);
		char b = 'b';
		System.out.print(b);
		char c = 'c';
		System.out.print(c);
		char d = (char)(c+1);
		System.out.print(d);
		System.out.println();
		System.out.println("Now I know my ABCs!");
	}
}